from ComputerPyGraphics import CG

cg = CG(25,25,".")

# print("Enter the value of xc: ")
# xc = int(input())
# print("Enter the value of yc: ")
# yc = int(input())
print("Enter the value of r: ")
r = int(input())

x,y = 0,r
d = 1-r

while(x<=y):
	cg.putpixel(x, y)
	cg.putpixel(-x, y)
	cg.putpixel(x, -y)
	cg.putpixel(-x, -y)
	cg.putpixel(y, x)
	cg.putpixel(-y, x)
	cg.putpixel(y, -x)
	cg.putpixel(-y, -x)
	if d<0:
	    d = d + 2*x + 1
	    x += 1
	else:
	    d = d + 2*(x-y) + 1
	    x += 1
	    y -= 1

cg.show()

