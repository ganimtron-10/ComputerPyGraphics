from ComputerPyGraphics import CG

cg = CG(50,50,'.')

def dda(x1,y1,x2,y2):
	dx = x2 - x1
	dy = y2 - y1
	length = max(abs(dx), abs(dy))

	xinc = dx/length
	yinc = dy/length

	xi = x1
	yi = y1

	for i in range(length+1):
		cg.putpixel(round(xi), round(yi))
		xi = xi + xinc
		yi = yi + yinc

def bca(r):
	x = 0
	y = r
	d = 3-(2*r)


	while(x<=y):
		cg.putpixel(x, y, "#")
		cg.putpixel(-x, y, "#")
		cg.putpixel(x, -y, "#")
		cg.putpixel(-x, -y, "#")
		cg.putpixel(y, x, "#")
		cg.putpixel(-y, x, "#")
		cg.putpixel(y, -x, "#")
		cg.putpixel(-y, -x, "#")
		if d<0:
		    d = d + 4*x + 6
		    x += 1
		else:
		    d = d + 4*x - 4*y + 10
		    x += 1
		    y -= 1

if __name__ == '__main__':
	ir = int(input("Enter inner circle radius: "))
	ora = 2*ir
	p = round(1.73 * ir)
	dda(0, ora, p, -ir)
	dda(0, ora, -p, -ir)
	dda(p,-ir,-p,-ir)
	bca(ir)
	bca(ora)
	cg.show()

